        SalaDao sDao = new SalaDao(cSQL);
        int aux = 0; 
        for (Sala s : sDao.listarTodasSalas()) {
            System.out.println(s.getNome());
            if(tfNomeSala.getText().equalsIgnoreCase(s.getNome())){
                aux++;
            }
        }
        if(aux == 0){
            Sala s = new Sala();
            s.setNome(tfNomeSala.getText());
            cSQL.conectar();
            sDao.inserirSala(s);
        }else{
            JOptionPane.showMessageDialog(null, "Sala j√° existe");
        }
        
        
